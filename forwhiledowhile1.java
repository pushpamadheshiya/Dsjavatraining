public class forwhiledowhile1 {
    public static void main(String[]args){

    
    int i=11;

    for (i=1;i<11;i++){
        System.out.print(i+" ");
    }
    System.out.println();

    while(i>1){
        i--;
        System.out.print(i+" ");
    }
    System.out.println();

    do{
        System.out.print(i+" ");
    
        i++;
     } while(i<11);
    
    
}

}
